package kosta.teamd.vo;

public class SurveyLogVO {
	private int num,sno;
	private String surip,surdate;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getSno() {
		return sno;
	}
	public void setSno(int sno) {
		this.sno = sno;
	}
	public String getSurip() {
		return surip;
	}
	public void setSurip(String surip) {
		this.surip = surip;
	}
	public String getSurdate() {
		return surdate;
	}
	public void setSurdate(String surdate) {
		this.surdate = surdate;
	}
	
}
